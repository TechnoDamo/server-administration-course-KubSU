#!/bin/bash

# Проверяем, передано ли название планеты
if [ "$#" -ne 1 ]; then
    echo "Использование: $0 <название_планеты>"
    exit 1
fi

planet=$1

# Создаём ассоциативный массив с количеством спутников
declare -A moons=(
    ["Меркурий"]=0
    ["Венера"]=0
    ["Земля"]=1
    ["Марс"]=2
    ["Юпитер"]=79
    ["Сатурн"]=83
    ["Уран"]=27
    ["Нептун"]=14
)

# Проверяем, существует ли планета в массиве
if [[ -n "${moons[$planet]}" ]]; then
    echo "$planet имеет ${moons[$planet]} спутников."
else
    echo "Ошибка: Планета '$planet' не существует."
fi
